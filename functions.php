/**
 * Enqueue styles and scripts
 */
function az_theme_enqueue_styles() {

	// Get the theme data.
	$the_theme     = wp_get_theme();
	$theme_version = $the_theme->get( 'Version' );

	// Register Theme main style.
	wp_enqueue_style( 'theme-styles', get_template_directory_uri() . '/dist/css/main.css', array(), $theme_version );

	// https://use.typekit.net/yjm6usa.css first loaded fonts library.
	wp_enqueue_style( 'typekit-fonts', 'https://use.typekit.net/yjm6usa.css', array(), null );

	wp_enqueue_script( 'jquery', false, array(), $theme_version, true );
	wp_enqueue_script( 'theme-scripts', get_stylesheet_directory_uri() . '/dist/js/main.js', array( 'jquery' ), $theme_version, true );

	if ( is_page_template( 'page-templates/page-arrival-contacts.php' ) || is_admin() ) :
		wp_enqueue_script( 'google-map-settings', get_stylesheet_directory_uri() . '/assets/js/google-maps.js', array( 'jquery' ), $theme_version, true );
		wp_enqueue_script( 'google-map-api', 'https://maps.googleapis.com/maps/api/js?key=' . AZ_GOOGLE_MAPS_API_KEY . '&callback=initMap&loading=async', array(), null, true );
	endif;

}

add_action( 'wp_enqueue_scripts', 'az_theme_enqueue_styles' );


/**
 * Load Typekit fonts asynchronously to avoid render-blocking.
 *
 * Intercepts the default <link rel="stylesheet"> tag generated by WordPress
 * for the 'typekit-fonts' handle and replaces it with a non-blocking preload
 * pattern. The preconnect hint is handled separately via az_theme_resource_hints().
 *
 * @param string $html   The full <link> tag HTML generated by WordPress.
 * @param string $handle The registered style handle.
 * @return string
 */
function az_typekit_non_blocking_loader( string $html, string $handle ): string {
	if ( 'typekit-fonts' !== $handle ) {
		return $html;
	}

	return '<link rel="preload" href="https://use.typekit.net/yjm6usa.css" as="style" onload="this.onload=null;this.rel=\'stylesheet\'">' . "\n"
		. '<noscript><link rel="stylesheet" href="https://use.typekit.net/yjm6usa.css"></noscript>' . "\n";
}
add_filter( 'style_loader_tag', 'az_typekit_non_blocking_loader', 10, 2 );

/**
 * Add preconnect and DNS-prefetch resource hints for external origins.
 *
 * Uses the WordPress resource hints API (wp_resource_hints) to register
 * early connection hints for third-party domains used by the theme.
 *
 * @param array  $hints         URLs to print for resource hints.
 * @param string $relation_type The relation type (preconnect, dns-prefetch, etc.).
 * @return array
 */
function az_theme_resource_hints( array $hints, string $relation_type ): array {
	if ( 'preconnect' === $relation_type ) {
		$hints[] = array(
			'href'        => 'https://use.typekit.net',
			'crossorigin' => 'anonymous',
		);
		$hints[] = array(
			'href' => 'https://maps.googleapis.com',
		);
	}

	if ( 'dns-prefetch' === $relation_type ) {
		$hints[] = array( 'href' => 'https://www.google-analytics.com' );
	}

	return $hints;
}
add_filter( 'wp_resource_hints', 'az_theme_resource_hints', 10, 2 );

