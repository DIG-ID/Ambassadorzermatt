/**
 * Logs data to the browser console for debugging purposes.
 *
 * @param mixed ...$data The data to log to the console.
 */
function my_console_log( ...$data ) {
	$json = wp_json_encode( $data );
	add_action(
		'shutdown',
		function () use ( $json ) {
			echo "<script>console.log({$json})</script>";
		}
	);
}
